<script>
var table, save_method;
    $(function () {
        // Menampilkan data dengan Datatables
        table = $('#tabelmanufacturer').DataTable({
            "processing": true,
            "serverside": true,
            dom: 'Bfrtip',
        buttons: [
            'copy', 'csv', 'excel'
        ],
            "ajax": {
                "url": "{{route('listManufacturer')}}",
                "type": "GET",
            }
        });
        $('.buttons-copy, .buttons-csv, .buttons-print, .buttons-pdf, .buttons-excel').addClass('btn btn-primary mr-1');

        //Menyimpan data 
        $('#manufacturerModal form').on('submit',function(e){
            if(!e.isDefaultPrevented()){
                var id = $('#id').val();
                if(save_method == "add") url = "{{route('manufacturer.store') }}";
                else url = "manufacturer/"+id;
                $.ajax({
                    url : url,
                    type : "POST",
                    data : $('#manufacturerModal form').serialize(),
                    success : function(data){
                        $('#manufacturerModal').modal('hide');
                        table.ajax.reload();
                    },
                    error : function(){
                        alert("Cannot Save Data");
                    }
                });
                return false;
            }
        });
    });
    //Menampilkan form tambah
    function addForm(){
        save_method = "add";
        $('input[name=_method]').val('POST');
        $('#manufacturerModal').modal('show');
        $('#manufacturerModal form')[0].reset();
        $('.modal-title').text('Tambah Manufacturer');
    }
    //Menampilkan data form Edit
    function editForm(id){
        save_method = "edit";
        $('input[name=_method]').val('PATCH');
        $('#manufacturerModal form')[0].reset();
        $.ajax({
            url : "manufacturer/"+id+"/edit",
            type : "GET",
            dataType : "JSON",
            success : function(data){
                $('#manufacturerModal').modal('show');
                $('.modal-title').text('Edit Manufacturer');

                $('#id').val(data.manufacturers_id);
                $('#manufacturers_name').val(data.manufacturers_name);
                $('#manufacturers_address').val(data.manufacturers_address);
                $('#contact_person').val(data.contact_person);
                $('#phone').val(data.phone);
                $('#email').val(data.email);

            },
            error : function(){
                alert("Cannot Show Data");
            }
        });
    }
    //Menghapus data
    function deleteData(id){
        if(confirm("Are you Sure?")){
            $.ajax({
                url : "manufacturer/"+id,
                type : "DELETE",
                data : {
                    'method' : 'DELETE',
                    "_token": "{{ csrf_token() }}"

                    },
                    success : function(data){
                         table.ajax.reload();
                    },
                    error : function(){
                        alert("Cannot Delete Data");
                    }
            });
        }
    }
</script>

